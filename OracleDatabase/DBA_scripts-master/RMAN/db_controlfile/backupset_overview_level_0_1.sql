alter session set nls_date_format = 'YYYYMMDD HH24:MI:SS';
select session_key, device_type, min(START_TIME) as start_time, max(COMPLETION_TIME) as end_time, 
  round((max(COMPLETION_TIME) - min(START_TIME))* 24,2) hours,
  sum ( round(output_bytes/1e9,2) ) as gb
from v$backup_set_details
where start_time>sysdate-55 and incremental_level=&level
group by session_key, device_type
order by START_TIME
;

